/*
Create 4 math functions, one called add() that adds 2 numbers, 
one called subtract() that subtracts 2 numbers, one called 
multiply() to multiply 2 numbers, 
and one called divide() to divide two numbers.
*/

function add(num1,num2){
     return num1 + num2;
}
function subtract(num1,num2){
    return num1 - num2;
}

function multiply(num1,num2){
        return num1 * num2;
}

function divide(num1,num2){
    return num1 / num2;
}
